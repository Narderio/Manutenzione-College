package org.elis.manutenzione.model._enum;

/**
 * Enumerazione che definisce i tipi di messaggio possibili.
 */
public enum TipoMessaggio {
    /**
     * Indica un messaggio di invio.
     */
    INVIO,
    /**
     * Indica un messaggio di modifica.
     */
    MODIFICA,
    /**
     * Indica un messaggio di eliminazione.
     */
    ELIMINAZIONE;
}